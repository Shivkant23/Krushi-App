class FarmerData{
  String fullName;
  String gender;
  String address;
  double plotArea;
  String crop;
  String variety;
  String plantingDate;
  int ageOfCrop;

  FarmerData({
    required this.fullName,
    required this.gender,
    required this.address,
    required this.plotArea,
    required this.crop,
    required this.variety,
    required this.plantingDate,
    required this.ageOfCrop
  });
}